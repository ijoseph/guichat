package client;

public class ModelInconsistencyException extends Exception {

	private static final long serialVersionUID = 5892681239536657970L;

	public ModelInconsistencyException() {
	}

	public ModelInconsistencyException(String arg0) {
		super(arg0);
	}

	public ModelInconsistencyException(Throwable arg0) {
		super(arg0);
	}

	public ModelInconsistencyException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
